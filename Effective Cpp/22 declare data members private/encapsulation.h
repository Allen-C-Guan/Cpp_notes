#ifndef EFFECTIVE_CPP_ENCAPSULATION_H
#define EFFECTIVE_CPP_ENCAPSULATION_H

/*
 * ******************* 封装的好处！ **************
 * 中心思想： 封装意味着灵活！和安全！
 * 举例：
 * 即使连最简单的setter和getter函数都可以从封装中获益！
 * 比如我们有setter和getter，最开始的时候只是单纯的获取某个私有成员变量，但是随着业务的扩张，我们想获取的不再是成员变量
 * 而是一个算法的结果，比如平均值，最大值等，这时候我们只需要更改getter函数即可，用户丝毫不知道我们更改了什么，他们只负责
 * 用就行了！
 *
 * 反观如果我们public某个私有成员，我们就没法继续扩张了，因为外面全都是这么用的，我们没有丝毫隐私可言！
 *
 *
 *
 *
 *
 */





#endif //EFFECTIVE_CPP_ENCAPSULATION_H
